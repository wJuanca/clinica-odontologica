    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rutaCarpetaSeguros = System.getProperty("user.home") + "/Desktop/seguros";

    // Creamos un objeto File con la ruta de la carpeta
    File carpetaSeguros = new File(rutaCarpetaSeguros);

    try {
        // Verificamos si Desktop es compatible con la plataforma actual
        if (Desktop.isDesktopSupported() && carpetaSeguros.exists()) {
            // Abre la carpeta y con el estilo del sistema
            Desktop.getDesktop().open(carpetaSeguros);
        } else {
            // Si la carpeta no existe mostrar ese mensaje
            javax.swing.JOptionPane.showMessageDialog(this, "Verifique que la carpeta¬†'seguros' existe en el escritorio.");
        }
    } catch (IOException e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_jButton1ActionPerformed

