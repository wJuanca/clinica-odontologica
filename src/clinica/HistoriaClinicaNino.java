    private void buscapacienteKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_buscapacienteKeyPressed
        // TODO add your handling code here:
            
        DefaultTableModel modelotabla = (DefaultTableModel) tablapacientenino.getModel();
                
        modelotabla.setRowCount(0);
        
        PreparedStatement ps;
        ResultSet rs;
        ResultSetMetaData rsmd;
        
        int columnas;
        
        String sql = "SELECT Cod_paciente,Id_paciente ,Nombre, Apellido, Fecha_nacimiento, Nacionalidad, Telefono, Direccion, Peso, Talla, Edad FROM Paciente WHERE Id_paciente like '%"+buscapaciente.getText()+"%' or Nombre like '%"+buscapaciente.getText()+"%' or Apellido like '%"+buscapaciente.getText()+"%' ";
        
        try {
            
            Connection con = cone.obtenerconexion();
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            rsmd = rs.getMetaData();
            
            columnas = rsmd.getColumnCount();
            
            while (rs.next()) {
                Object[] fila = new Object[columnas];
                for (int i = 0; i < columnas; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                modelotabla.addRow(fila);
            }
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_buscapacienteKeyPressed

