    private void tablapacienteninoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablapacienteninoMouseClicked
        // TODO add your handling code here:
         try
        {
            fila = tablapacientenino.getSelectedRow();
            codigo = Integer.parseInt(tablapacientenino.getValueAt(fila, 0).toString());
            
            PreparedStatement ps;
            ResultSet rs;
            
            Connection con = cone.obtenerconexion();
            
            //--ps = con.prepareStatement("select Cod_paciente, Nombre , Fecha_Nacimiento, Nacionalidad, Telefono, Direccion, Peso, Talla, Edad from Paciente where Cod_paciente = ?");
            //--ps.setInt(1, codigo);
            CallableStatement cmd = con.prepareCall("{call mostrarninoesp(?)}");
            
            cmd.setInt(1, codigo);
            rs = cmd.executeQuery();
            //--rs = ps.executeQuery();
            while(rs.next())
            {
                //codigonino = rs.getInt("Cod_paciente");
                //String fecha = rs.getString("Fechanacimiento");
                String fecha = rs.getString("Fecha_Nacimiento");
                
                txtnombrecompleto.setText(rs.getString("Nombre") + " " + rs.getString("Apellido"));
                fechanacimiento.setDate(Date.valueOf(fecha));
                txtnacionalidad.setText(rs.getString("Nacionalidad"));  
                txttelefono.setText(rs.getString("Telefono"));
                txtdomicilio.setText(rs.getString("Direccion"));
                txtpeso.setText(rs.getString("Peso"));
                txttalla.setText(rs.getString("Talla"));
                txtedad.setText(rs.getString("Edad"));
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_tablapacienteninoMouseClicked

